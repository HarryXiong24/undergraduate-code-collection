print('a | b  or_(a, b)')
print('0 | 0 ->', 0 | 0)
print('1 | 1 ->', 1 | 1)
print('0 | 1 ->', 0 | 1)
print('1 | 0 ->', 1 | 0)

# 當兩數值相同時為False，而數值不同時為 True
print('a ^ b  xor(a, b)')
print('0 ^ 0 ->', 0 ^ 0)
print('1 ^ 1 ->', 1 ^ 1)
print('0 ^ 1 ->', 0 ^ 1)
print('1 ^ 0 ->', 1 ^ 0)